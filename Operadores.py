print("Introduce dos números a comparar: \n")

num_1 = int(input("Introduce el primer número: "))
num_2 = int(input("Introduce el segundo número: "))

print("\n Los números a comparar son: ", num_1, "y", num_2, "\n")

if num_1 == num_2 :
    print("Es igual...")
if num_1 != num_2 :
    print("Es diferente...")
if num_1 >= num_2 :
    print("Es mayor o igual...")
if num_1 <= num_2 :
    print("Es menor o igual...")
if num_1 < num_2 :
    print("Es menor...")
if num_1 > num_2 :
    print("Es mayor...")
else:
    print("No ingreso datos válidos.")

print("Fin.")    
    



      
